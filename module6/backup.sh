#!/bin/bash
# =============================================================================
# backup.sh — Скрипт резервного копіювання логів
# Використання: ./backup.sh <log_dir> <backup_dir>
# =============================================================================

# --- Константи ----------------------------------------------------------------
LOCK_FILE="/tmp/backup.lock"

# =============================================================================
# 1. ПЕРЕВІРКА АРГУМЕНТІВ
# =============================================================================

# Перевірка кількості переданих аргументів
if [ "$#" -ne 2 ]; then
    echo "Usage: ./backup.sh <log_dir> <backup_dir>"
    exit 1
fi

LOG_DIR="$1"
BACKUP_DIR="$2"

# Перевірка, що обидва аргументи є існуючими каталогами
if [ ! -d "$LOG_DIR" ] || [ ! -d "$BACKUP_DIR" ]; then
    echo "Usage: ./backup.sh <log_dir> <backup_dir>"
    exit 1
fi

# =============================================================================
# 2. ЗАХИСТ ВІД ПАРАЛЕЛЬНОГО ЗАПУСКУ (lock-файл)
# =============================================================================

# Якщо lock-файл вже існує — інший екземпляр скрипта запущено
if [ -f "$LOCK_FILE" ]; then
    echo "Backup already running"
    exit 1
fi

# Створюємо lock-файл і реєструємо його автоматичне видалення при виході
touch "$LOCK_FILE"
trap "rm -f '$LOCK_FILE'" EXIT

# =============================================================================
# 3. СТВОРЕННЯ АРХІВУ ЛОГІВ
# =============================================================================

# Формуємо ім'я архіву з поточною датою і часом
TIMESTAMP=$(date +"%Y-%m-%d_%H-%M")
ARCHIVE_NAME="logs_backup_${TIMESTAMP}.tar.gz"
ARCHIVE_PATH="${BACKUP_DIR}/${ARCHIVE_NAME}"

# Архівуємо всі файли з каталогу логів
tar -czf "$ARCHIVE_PATH" -C "$LOG_DIR" .

# =============================================================================
# 4. ПЕРЕВІРКА РЕЗУЛЬТАТУ АРХІВАЦІЇ
# =============================================================================

# Перевіряємо код завершення команди tar
if [ $? -ne 0 ]; then
    echo "Backup failed"
    # Видаляємо пошкоджений архів, якщо він був частково створений
    rm -f "$ARCHIVE_PATH"
    exit 2
fi

# Виводимо повний шлях до успішно створеного архіву
echo "Backup created: $(realpath "$ARCHIVE_PATH")"
